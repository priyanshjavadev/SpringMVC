package com.QDev.springmvc.springmvc_mybatis.model;

public class Employee {

		private int id;
		private String name;
		private String address;
		private String city;
		private int age;
		private long no;
		
		public long getNo() {
			return no;
		}
		public void setNo(long no) {
			this.no = no;
		}
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getAddress() {
			return address;
		}
		public void setAddress(String address) {
			this.address = address;
		}
		public String getCity() {
			return city;
		}
		public void setCity(String city) {
			this.city = city;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		
		
		
		
		
		
		
}
